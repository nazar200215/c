struct Student
{
	char m_2name[20];
	int m_bd_year;
};

int GetYoungList(const Student* ip_list, int list_size, int start_year, Student* op_young_list)
{
	int res = 0;
	for (int i = 0; i < list_size; ++i)
	{
		if (ip_list->m_bd_year >= start_year)
		{
			++res;
			if (op_young_list != nullptr)
			{
				strcpy_s(op_young_list->m_2name, ip_list->m_2name);
				op_young_list->m_bd_year = ip_list->m_bd_year;
				++op_young_list;
			}
		}
		++ip_list;
	}
	return res;
}

void PrintList(Student *p_list, int count)
{
	for (int i = 0; i < count; ++i)
	{
		std::cout << p_list->m_2name << ", " << p_list->m_bd_year << std::endl;
		++p_list;
	}
}

